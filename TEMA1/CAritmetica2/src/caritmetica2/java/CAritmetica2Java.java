public class CAritmetica2Java {

    /**
     * @param args
     */
    public static void main(String[] args) {
        int dato1;
        int dato;
        int resultado;
        int dato3;
       
        dato1 = 20;
        dato2 = 10;
        dato3 = 5;
        
        //suma
        resultado = dato1 + dato2 + dato3;
        System.out.println(dato1 + " + " + dato2 + " + " + dato3 + " = " + resultado);
        
        //resta
        resultado = dato1 - dato2 - dato3;
        System.out.println(dato1 + " - " + dato2 + " - " + dato3 + " = " + resultado);
        
        //producto
        resultado = dato1 * dato2 * dato3;
        System.out.println(dato1 + " * " + dato2 + " * " + dato3 + " = " + resultado);
        
        //cociente
        resultado = dato1 / dato2 / dato3;
        System.out.println(dato1 + " / " + dato2 + " / " + dato3 + " = " + resultado);
        
    }
}
